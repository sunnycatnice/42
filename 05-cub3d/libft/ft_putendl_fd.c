#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int i;

	if (!s || !fd)
		return ;
	i = -1;
	while (s[++i])
		ft_putchar_fd(s[i], fd);
	ft_putchar_fd('\n', fd);
}
